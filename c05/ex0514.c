#include <stdio.h>
#define VALUEA 8
#define VALUEB 2

int	main(void)
{
	printf("Values %d and %d\n", VALUEA, VALUEB);
	printf("Addition is: %d\n", VALUEA+VALUEB);
	printf("Subtraction is: %d\n", VALUEA-VALUEB);
	printf("Multiplication is: %d\n", VALUEA*VALUEB);
	printf("Division is: %d\n", VALUEA/VALUEB);
	return(0);
}
